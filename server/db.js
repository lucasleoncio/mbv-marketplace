// ---------------------------------------------------------------------------
// Driver SQLite (better-sqlite3 → fallback node:sqlite), promisificado
// ---------------------------------------------------------------------------
function sqliteDriver() {
  const DATA_DIR = process.env.MBV_DATA_DIR || path.join(__dirname, 'data');
  if (!fs.existsSync(DATA_DIR)) fs.mkdirSync(DATA_DIR, { recursive: true });
  const DB_PATH = path.join(DATA_DIR, 'mbv.db');

  let raw, driverName;
  try {
    const Database = require('better-sqlite3');
    raw = new Database(DB_PATH);
    try { raw.pragma('journal_mode = WAL'); } catch (_) {}
    try { raw.pragma('foreign_keys = ON'); } catch (_) {}
    driverName = 'better-sqlite3';
  } catch (_) {
    let DatabaseSync;
    try { ({ DatabaseSync } = require('node:sqlite')); } catch (builtinErr) {
      console.error('\n[MBV] Nenhum driver de banco pôde ser carregado.');
      console.error('  • Node 22.5 a 23.3:  rode  "npm run start:builtin"');
      console.error('  • Node 24+:          rode  "npm start"');
      console.error('  • Alternativa:       "xcode-select --install" e "npm install" de novo\n');
      throw builtinErr;
    }
    const d = new DatabaseSync(DB_PATH);
    try { d.exec('PRAGMA journal_mode = WAL'); } catch (_) {}
    try { d.exec('PRAGMA foreign_keys = ON'); } catch (_) {}
    raw = {
      exec: (sql) => d.exec(sql),
      prepare: (sql) => {
        const stmt = d.prepare(sql);
        return {
          get: (...a) => stmt.get(...a),
          all: (...a) => stmt.all(...a),
          run: (...a) => { const r = stmt.run(...a); return { changes: Number(r.changes), lastInsertRowid: Number(r.lastInsertRowid) }; }
        };
      }
    };
    driverName = 'node:sqlite';
  }

  raw.exec(schemaFor(false));
  for (const stmt of SQLITE_MIGRATIONS) { try { raw.exec(stmt); } catch (_) {} }

  const api = {
    isPg: false,
    LIKE: 'LIKE',
    __driver: driverName,
    ready: Promise.resolve(),
    exec: async (sql) => raw.exec(sql),
    prepare(sql) {
      return {
        get: async (...a) => raw.prepare(sql).get(...a),
        all: async (...a) => raw.prepare(sql).all(...a),
        run: async (...a) => raw.prepare(sql).run(...a)
      };
    },
    // Compatível com better-sqlite3: fn pode ser async, mas deve conter APENAS
    // operações de banco (awaits resolvidos viram microtasks — sem interleaving).
    transaction: (fn) => async (...args) => {
      raw.exec('BEGIN');
      try { const out = await fn(...args); raw.exec('COMMIT'); return out; }
      catch (e) { try { raw.exec('ROLLBACK'); } catch (_) {} throw e; }
    }
  };
  return api;
}

// ---------------------------------------------------------------------------
// Driver Postgres (pg) — tradução de placeholders + transação via AsyncLocalStorage
// ---------------------------------------------------------------------------
function pgDriver() {
  const { Pool, types } = require('pg');
  const { AsyncLocalStorage } = require('async_hooks');
  // int8 (COUNT/SUM) e numeric chegam como string por padrão — normalizamos p/ Number
  types.setTypeParser(20, (v) => (v === null ? null : Number(v)));
  types.setTypeParser(1700, (v) => (v === null ? null : Number(v)));

  const local = /@(localhost|127\.0\.0\.1)/.test(DATABASE_URL) || DATABASE_URL.includes('sslmode=disable');
  const pool = new Pool({ connectionString: DATABASE_URL, max: 10, ssl: local ? undefined : { rejectUnauthorized: false } });
  pool.on('error', (e) => console.error('[pg pool]', e.message));
  const als = new AsyncLocalStorage();
  const client = () => (als.getStore() && als.getStore().client) || pool;

  // `?` → $n · `@nome` (objeto) → $n. Mantém a semântica do SQLite.
  function translate(sql, args) {
    const isObj = args.length === 1 && args[0] && typeof args[0] === 'object' && !Array.isArray(args[0]);
    if (isObj && /@[a-zA-Z_]/.test(sql)) {
      const obj = args[0]; const names = [];
      const text = sql.replace(/@([a-zA-Z_][a-zA-Z0-9_]*)/g, (_, n) => {
        let i = names.indexOf(n);
        if (i === -1) { names.push(n); i = names.length - 1; }
        return '$' + (i + 1);
      });
      return { text, values: names.map((n) => obj[n]) };
    }
    let i = 0;
    const text = sql.replace(/\?/g, () => '$' + (++i));
    // Objeto passado a um SQL sem placeholders (padrão do SQLite p/ filtros dinâmicos
    // vazios): não há o que bindar — descarta em vez de mandar 1 parâmetro a mais.
    const values = i === 0 && isObj ? [] : args;
    return { text, values };
  }

  async function query(sql, args) {
    const { text, values } = translate(sql, args);
    return client().query(text, values);
  }

  const api = {
    isPg: true,
    LIKE: 'ILIKE',
    __driver: 'postgres',
    exec: async (sql) => { await client().query(sql); },
    prepare(sql) {
      return {
        get: async (...a) => (await query(sql, a)).rows[0],
        all: async (...a) => (await query(sql, a)).rows,
        run: async (...a) => {
          let s = sql;
          // lastInsertRowid: INSERTs ganham RETURNING id automaticamente
          const wantsId = /^\s*insert\b/i.test(s) && !/returning/i.test(s);
          if (wantsId) s += ' RETURNING id';
          const r = await query(s, a);
          return { changes: r.rowCount || 0, lastInsertRowid: wantsId && r.rows[0] ? Number(r.rows[0].id) : 0 };
        }
      };
    },
    transaction: (fn) => async (...args) => {
      const c = await pool.connect();
      try {
        await c.query('BEGIN');
        const out = await als.run({ client: c }, () => fn(...args));
        await c.query('COMMIT');
        return out;
      } catch (e) {
        try { await c.query('ROLLBACK'); } catch (_) {}
        throw e;
      } finally { c.release(); }
    },
    end: () => pool.end()
  };
  api.ready = (async () => {
    await pool.query(schemaFor(true));
    console.log('  [db] Postgres conectado (schema verificado).');
  })();
  api.ready.catch((e) => console.error('  [db] ⚠️ Falha ao preparar o Postgres:', e.message));
  return api;
}

const db = DATABASE_URL ? pgDriver() : sqliteDriver();

// Recalcula o índice de busca normalizado (sem acento) de todos os produtos.
db.reindexSearch = async function () {
  try {
    const { normalize } = require('./lib/text');
    const prods = await db.prepare('SELECT id, name, description, badges FROM products').all();
    for (const p of prods) {
      let badges = ''; try { badges = (JSON.parse(p.badges || '[]') || []).join(' '); } catch (_) {}
      await db.prepare('UPDATE products SET search_text = ? WHERE id = ?')
        .run(normalize(`${p.name} ${p.description || ''} ${badges}`), p.id);
    }
  } catch (e) { console.error('[reindex]', e.message); }
};

// Data/hora UTC no formato canônico dos dois drivers ("YYYY-MM-DD HH:MM:SS").
db.utcNow = function (offsetMs = 0) {
  return new Date(Date.now() + offsetMs).toISOString().slice(0, 19).replace('T', ' ');
};

