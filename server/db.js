// ============================================================================
// Camada de dados do MBV — DUAL DRIVER com API ÚNICA e ASSÍNCRONA.
//
//   SQLite  (padrão)        → demo/local, zero configuração (better-sqlite3
//                             com fallback para node:sqlite embutido no Node).
//   Postgres (DATABASE_URL) → produção (Render Postgres). Ativa sozinho quando
//                             a env existe. Rollback = remover a env.
//
// API (idêntica nos dois drivers — SEMPRE await):
//   db.prepare(sql).get(params?)  -> Promise<row | undefined>
//   db.prepare(sql).all(params?)  -> Promise<rows[]>
//   db.prepare(sql).run(params?)  -> Promise<{ changes, lastInsertRowid }>
//   db.exec(sql)                  -> Promise (DDL / múltiplos statements)
//   db.transaction(fn)            -> fn async; retorna função async (compatível
//                                    com o padrão `const run = db.transaction(...); await run()`)
//   db.reindexSearch()            -> Promise
//   db.ready                      -> Promise (schema criado)
//   db.LIKE                       -> 'LIKE' (SQLite) | 'ILIKE' (PG) p/ busca case-insensitive
//   db.isPg                       -> boolean
//
// Regras de ouro:
//   • Placeholders: `?` posicionais ou `@nome` com objeto — traduzidos p/ $n no PG.
//   • Datas são gravadas como TEXTO UTC "YYYY-MM-DD HH:MM:SS" nos DOIS drivers
//     (mesmo formato do datetime('now') do SQLite) — comparações de string e o
//     frontend continuam funcionando sem mudança.
//   • Dentro de db.transaction(), use SOMENTE operações de banco (nada de fetch/
//     timers) — no SQLite isso garante não-interleaving; no PG o client é dedicado.
// ============================================================================
